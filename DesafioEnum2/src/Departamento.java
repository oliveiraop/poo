public class Departamento {
    private String nome;
    private int numero;
    private String sigla;

    public Departamento(String nome, int numero, String sigla) {
        this.nome = nome;
        this.numero = numero;
        this.sigla = sigla;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getNumero() {
        return numero;
    }

    public void setNumero(int numero) {
        this.numero = numero;
    }

    public String getSigla() {
        return sigla;
    }

    public void setSigla(String sigla) {
        this.sigla = sigla;
    }
}
